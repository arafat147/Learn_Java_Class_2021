package basicJava;

public class Blocks {

	
	{//Block start point
		
		//Block body
		System.out.println("This is a block");//this block is not executable.
		
	}//Block end point
	
	public static void main(String[] args) {
		
		
		{//Block start point
			
			//Block body
			System.out.println("This is a block");//this block is executable cz it's inside the main method.
			
		}//Block end point
		
		
		
		
		
		
	}
	
	
}
